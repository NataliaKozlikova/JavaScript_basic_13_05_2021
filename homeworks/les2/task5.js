/* 5.Реализовать четыре основные арифметические операции в виде функций с двумя параметрами. 
Обязательно использовать оператор return.
*/

'use stcrict'

function sum(a, b) {
    return a + b;
}

function minus(a, b) {
    return a - b;
}

function mult(a, b) {
    return a * b;
}

function div(a, b) {
    return a / b;
}

console.log(sum(4, 2)); // 6
console.log(minus(4, 2)); // 2
console.log(mult(4, 2)); // 8
console.log(div(4, 2)); // 2